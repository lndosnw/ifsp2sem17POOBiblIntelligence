    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        btResetSenha.setVisible(false);
        
    //inicio do preenchimento do listbox
        cbGrupo.removeAllItems();
        try{
            while(Conexao.getResultSet("SELECT nomeGrupo from gruposeg").next()){ 
			String nome = Conexao.getRsString("nomeGrupo");
                        cbGrupo.addItem(nome);
		  }
                  
		  } 
		  catch(SQLException ex){
			   System.out.println("SQLException: " + ex.getMessage());
			   System.out.println("SQLState: " + ex.getSQLState());
			   System.out.println("VendorError: " + ex.getErrorCode());
		  }
		  catch(Exception e){
			System.out.println("Problemas ao tentar conectar com o banco de dados");	
		}
                finally{
                    try{
                        Conexao.finalizaUso(); //finaliza o uso da conexao
                    }catch(SQLException onConClose){
                    //System.out.println("Houve erro no fechamento da conexão");
                    JOptionPane.showMessageDialog(null,"Erro na conexão, com o banco de dados!","Erro Ao Logar",JOptionPane.WARNING_MESSAGE);
                    onConClose.printStackTrace();
                    }    
        }
    //fim do preeenchimenmto do listbox

        if(edit==1){
            tfUsuario.setText(usuario);
            tfEmail.setText(email);
            tfNome.setText(nome);
            tfEndereco.setText(endereco);
            //combobox grupo
            jfTelefone.setValue(telefone);
            btResetSenha.setVisible(true);
        }
    }//GEN-LAST:event_formWindowActivated

