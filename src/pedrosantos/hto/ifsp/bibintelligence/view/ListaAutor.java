    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed
        // TODO add your handling code here:
        CadAutor cadautor = CadAutor.getInstancia();
        cadautor.edit=1;
        if(tblAutores.getSelectedRow() != -1){
            
            cadautor.nome = (String) modelo.getValueAt(tblAutores.getSelectedRow(), 1);
            cadautor.descricao = (String) modelo.getValueAt(tblAutores.getSelectedRow(), 2);
            cadautor.idAutor = BibIntelligence.VerificaID("SELECT idAutor from autores where nome= \""+cadautor.nome+"\"", "idAutor",0);
            cadautor.setVisible(true);
        
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione Um Autor Para Editar", "Erro !", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btEditarActionPerformed

